#ifndef ENUM_HEADER
#define ENUM_HEADER
/**
 * enTypeList - enum different args data types
 * @enum - enumerates different types of args
 * @character: for char types
 * @integer: for int types
 * @floatingPoint: for floats or doubles
 * @string: for string(array of chars) types
 */

typedef enum
{
character = 'c',
integer = 'i',
floatingPoint = 'f',
string = 's'
} enTypeList;

#endif /* ENUM_HEADER */
