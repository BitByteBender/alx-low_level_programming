#include "3-calc.h"
/**
 * op_add - function for adding 2 numbers
 * @a: integer number
 * @b: integer number
 * Return: result of addition of 2 integers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function for subtracting 2 numbers
 * @a: integer number
 * @b: integer number
 * Return: result of subtraction of 2 integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function for multiplying 2 numbers
 * @a: integer number
 * @b: integer number
 * Return: result of multiplication of 2 integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function for dividing 2 numbers
 * @a: integer number
 * @b: integer number
 * Return: result of division calc of 2 integers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function for Mod calc of 2 numbers
 * @a: integer number
 * @b: integer number
 * Return: result of Mod of 2 integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
